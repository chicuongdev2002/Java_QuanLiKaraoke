    private void DocDuLieuVaoTable() {
        if (dsdv.size() == 0) {
            JOptionPane.showMessageDialog(null, "Không có dịch vụ trong máy!");
            mdlDonVi.addRow(new Object[]{"Không có thông tin", "Không có thông tin", "Không có thông tin"});
            return;
        }
        for (DonViDichVu item : dsdv) {
            mdlDonVi.addRow(new Object[]{item.getMaDonVi(), item.getTenDonVi(), item.getMaLoaiDV().getTenLoaiDichVu()});
        }
    }

    private void themDonViDichVu() {
        String tenDonVi = txtTenDonVi.getText().trim();
        String tenLoai = cboLoaiDichVu.getSelectedItem().toString().trim();
        int maLoai = ldv_dao.getMaByTenLoaiDichVu(tenLoai);
//        System.out.println(maLoai);
//        DonViDichVu dvdv = new DonViDichVu(tenDonVi, ldv);
        boolean them = dv_dao.ThemDonVi(tenDonVi, maLoai);
        if (them == true) {
            JOptionPane.showMessageDialog(null, "Thêm dịch vụ thành công");
        } else {
            JOptionPane.showMessageDialog(null, "Thêm dịch vụ thất bại ");
        }
    }

    private void updateTable() {
        mdlDonVi.setRowCount(0);
        for (DonViDichVu dv : dv_dao.getAllDonVi()) {
            Object[] row = {dv.getMaDonVi(), dv.getTenDonVi(), dv.getMaLoaiDV().getTenLoaiDichVu()};
            mdlDonVi.addRow(row);
        }
    }

    private void CapNhatDonViDichVu() {
        String maDV = txtMaDonVi.getText().trim().equals("") ? "0" : txtMaDonVi.getText().trim();
        int ma = Integer.parseInt(maDV);
        String tenDonVi = txtTenDonVi.getText().trim();
        String tenLoai = cboLoaiDichVu.getSelectedItem().toString();
        int maLoai = ldv_dao.getMaByTenLoaiDichVu(tenLoai);
        boolean update = dv_dao.CapNhatDonViDichVu(ma, tenDonVi, maLoai);
        if (update == true) {
            updateTable();
            JOptionPane.showMessageDialog(null, "Cập nhật đơn vị dịch vụ thành công");
        } else {
            JOptionPane.showMessageDialog(null, "Lỗi cập nhật không thành công");
        }
    }

    private void xoaTrang() {
        txtMaDonVi.setText("");
        txtTenDonVi.setText("");
        cboLoaiDichVu.setSelectedIndex(0);
    }

    private void XoaDonViDichVu() {
        int row = tblDonVi.getSelectedRow();
        try {
            if (row == -1) {
                JOptionPane.showMessageDialog(null, "Bạn cần chọn dòng cần xóa");
            } else {
                String maDV = txtMaDonVi.getText().trim().equals("") ? "0" : txtMaDonVi.getText().trim();
                int ma = Integer.parseInt(maDV);
                int select = JOptionPane.NO_OPTION;
                select = JOptionPane.showConfirmDialog(this, "Cảnh báo",
                        "Cảnh báo", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
                if (select == JOptionPane.YES_OPTION) {
                    dv_dao.XoaDonVi(ma);
                    mdlDonVi.removeRow(row);
                    updateTable();
                    JOptionPane.showMessageDialog(null, "Xóa thành công");
                }
            }
        } catch (Exception e3) {
            JOptionPane.showMessageDialog(null, "Xóa loại dịch vụ thất bại");
        }
    }
